// Em JavaScript, quase tudo é uma função, incluindo objetos, classes e até mesmo funções em si. Isso ocorre porque as funções são tratadas como cidadãos de primeira classe na linguagem, o que significa que podem ser atribuídas a variáveis, passadas como argumentos para outras funções e retornadas como valores de outras funções. Portanto, é comum encontrar muitos tipos de dados em JavaScript que são, na verdade, funções.

