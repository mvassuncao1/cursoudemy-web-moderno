console.log(typeof Math); //object
console.log(Math.PI); //constante PI
console.log(Math.E); //constante E
console.log(Math.sqrt(16)); //raiz quadrada
console.log(Math.pow(2, 10)); //potenciação (base, expoente)
console.log(2 ** 10); //potenciação (base, expoente)
console.log(Math.abs(-7.5)); //valor absoluto 7.5
console.log(Math.ceil(6.1)); //arredonda para cima
console.log(Math.floor(6.9)); //arredonda para baixo
console.log(Math.round(6.5)); //arredonda para o inteiro mais próximo
console.log(Math.max(3, 5, 1, 8, 2)); //retorna o maior valor
console.log(Math.min(3, 5, 1, 8, 2)); //retorna o menor valor
console.log(Math.random()); //gera um número aleatório entre 0 (inclusive) e 1 (exclusive)
console.log(Math.random() * 10); //gera um número aleatório entre 0 (inclusive) e 10 (exclusive)
console.log(Math.floor(Math.random() * 10)); //gera um número inteiro aleatório entre 0 (inclusive) e 10 (exclusive)